// Los operadores de comparación se utilizan para comparar 
// distintas variables entre si o una variable con otras cosas.
// Cada vez que utilizemos un operador de comparación
// este nos va a devolver un boolean.


// operaradores relacionales
let a = 10;
console.log(a > 5);
console.log(a >= 5);
console.log(a < 5);
console.log(a <= 5);

// operadores de igualdad
console.log(a == 10);
console.log(a != 10);
console.log(a == '10');
console.log(a === '10');
console.log(a !== '10');